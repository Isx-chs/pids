package model;

public class Modulos {
	private int mod_id;
	private String mod_nombre;
	private String mod_key;
	private String mod_link;
	
	
	
	public String getMod_link() {
		return mod_link;
	}
	public void setMod_link(String mod_link) {
		this.mod_link = mod_link;
	}
	public int getMod_id() {
		return mod_id;
	}
	public void setMod_id(int mod_id) {
		this.mod_id = mod_id;
	}
	public String getMod_nombre() {
		return mod_nombre;
	}
	public void setMod_nombre(String mod_nombre) {
		this.mod_nombre = mod_nombre;
	}
	public String getMod_key() {
		return mod_key;
	}
	public void setMod_key(String mod_key) {
		this.mod_key = mod_key;
	}
	
	
}
