package model;

public class Usuario {

	private int us_id;
	private String us_user;
	private String us_pass;
	private String us_nombre;
	private String us_apellido;
	private int us_per_id;
	private int us_area_id;
	
	
	public int getUs_id() {
		return us_id;
	}
	public void setUs_id(int us_id) {
		this.us_id = us_id;
	}
	public String getUs_nombre() {
		return us_nombre;
	}
	public void setUs_nombre(String us_nombre) {
		this.us_nombre = us_nombre;
	}
	public String getUs_apellido() {
		return us_apellido;
	}
	public void setUs_apellido(String us_apellido) {
		this.us_apellido = us_apellido;
	}
	public int getUs_per_id() {
		return us_per_id;
	}
	public void setUs_per_id(int us_per_id) {
		this.us_per_id = us_per_id;
	}
	public int getUs_area_id() {
		return us_area_id;
	}
	public void setUs_area_id(int us_area_id) {
		this.us_area_id = us_area_id;
	}
	public String getUs_user() {
		return us_user;
	}
	public void setUs_user(String us_user) {
		this.us_user = us_user;
	}
	public String getUs_pass() {
		return us_pass;
	}
	public void setUs_pass(String us_pass) {
		this.us_pass = us_pass;
	}
	
	
	
}
